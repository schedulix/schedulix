	public String getKey (SystemEnvironment env)
	throws SDMSException
	{
		return (key);
	}

	public	void setKey (SystemEnvironment env, String p_key)
	throws SDMSException
	{
		if(key.equals(p_key)) return;
		SDMSScopeConfigGeneric o;
		env.tx.beginSubTransaction(env);
		try {
			if (versions.id.longValue() < SystemEnvironment.SYSTEM_OBJECTS_BOUNDARY) {
				throw new CommonErrorException(
				        new SDMSMessage (env, "02112141636", "(ScopeConfig) Change of system object not allowed")
				);
			}
			o = (SDMSScopeConfigGeneric) change(env);
			if (p_key != null && p_key.length() > 64) {
				throw new CommonErrorException (
				        new SDMSMessage(env, "01112141510",
				                        "(ScopeConfig) Length of $1 exceeds maximum length $2", "key", "64")
				);
			}
			o.key = p_key;
			o.changerUId = env.cEnv.uid();
			o.changeTs = env.txTime();
			o.versions.table.index(env, o, 2);
			env.tx.commitSubTransaction(env);
		} catch (SDMSException e) {
			env.tx.rollbackSubTransaction(env);
			throw e;
		}
		return;
	}

